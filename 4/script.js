const age=22;

if(age>=22){
    console.log("Yes")
}else{
    console.log("NO");
}

// Ternary operator

age>=18 ? console.log("Yes") : console.log("NO")

// Switch

const key=1;

switch (key) {
    case 1:
        console.log('Namaste')
        break;

    case 2:
        console.log('Hello')
        break;

    default:
        console.log('Invalid')
        break;
}